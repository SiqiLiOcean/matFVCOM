%==========================================================================
% Convert longitude to [-180, 180]
%
% input  :
%   lon1 
% output :
%   lon2
% Siqi Li, SMAST
% 2022-03-31
%
% Updates:
%
%==========================================================================
function lon2 = calc_lon_180(lon1)

lon2 = mod(lon1+180, 360) - 180;
